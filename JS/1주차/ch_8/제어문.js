// 블록문
// - 블록문(block statement/compound statement)은 0개 이상의 문을 중괄호로 묶은 것으로, 코드 블로그 또는 블록이라함.

// 조건문
// - 조건문(conditional statement)은 주어진 조건식(conditional expression)의 평가 결과에 따라 코드블록의 실행을 결정함.
// - if ... else / switch 로 제공됨.

// 반복문
// - 반복문(loop statement)은 조건식의 평가 결과가 참인 경우 코드 블록을 실행함.
// - for, while, do ... while 으로 제공됨.
